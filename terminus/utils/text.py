def stopword_ratio(text: str, stopwords: set) -> float:
    """Compute the ratio of stop words to total tokens."""
    tokens = text.lower().split()
    if not tokens:
        return 0.0
    hits = sum(1 for t in tokens if t.strip(".,!?;:\"'«»—-") in stopwords)
    return hits / len(tokens)
